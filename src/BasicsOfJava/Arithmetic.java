package BasicsOfJava;

public class Arithmetic {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        System.out.println("The value of X is "+x);
        System.out.println("The value of Y is "+y);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
